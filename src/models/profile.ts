/**
 * Profile
 */
class Profile {

    /**
     * Get the profiles's id
     * @returns {string}
     */

    public id: string

    /**
     * Get the profiles's name
     * @returns {string}
     */

    public name: string

    /**
     * Get the profiles's tag
     * @returns {string}
     */

    public tag: string

    constructor() {
        this.id = ''
        this.name = ''
        this.tag = ''
    }
}